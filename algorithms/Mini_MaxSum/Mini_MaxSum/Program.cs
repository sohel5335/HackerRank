    public static void miniMaxSum(List<int> arr)
    {
        var rmin = arr;
        var rmax = arr;
        int a= rmin.Min();
        rmin.Remove(a);
        long max =rmin.Sum(x=>(long)x);
        rmax.Remove(rmax.Max());

        long min = rmax.Sum(x => (long)x) +a;

        Console.WriteLine(min + " " + max);
        //Console.ReadLine();

    }

}

